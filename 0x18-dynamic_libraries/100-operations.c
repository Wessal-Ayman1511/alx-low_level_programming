/**
 * add - adds two integers
 * @a: input
 * @b: input
 * Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - adds two integers
 * @a: input
 * @b: input
 * Return: addition
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - adds two integers
 * @a: input
 * @b: input
 * Return: addition
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - adds two integers
 * @a: input
 * @b: input
 * Return: addition
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - adds two integers
 * @a: input
 * @b: input
 * Return: addition
*/
int mod(int a, int b)
{
	return (a % b);
}
